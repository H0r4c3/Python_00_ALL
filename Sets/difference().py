

my_set1 = {1, 2, 3, 4, 5}
my_set2 = {5, 6, 7, 8, 9}
print(my_set1.difference(my_set2)) # elements in first set, but not in second set
print(my_set2.difference(my_set1)) # elements in second set, but not in first set
print(my_set1 - my_set2) # set difference
print(my_set2 - my_set1) # set difference